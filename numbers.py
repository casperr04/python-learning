# You can print numbers without making it a string
print(2)
print(2.015)
print(3+6)
print(3 * 10)
print(4 / 2)
print(2+(3-1))
# % lets you to get the remaining number in division
print(10 % 4)
# Variables let you store strings
my_number = 3
print(my_number)
print(my_number + 2)
# (str lets you print numbers in a string
print(str(my_number) + " is three")
# abs lets you get the absolute value of a number
my_number2 = -2
print(abs(my_number2))
# pow lets you power a number with two values
print(pow(4, 3))
# max lets you find the largest number, min the smallest number
print(max(4, 6))
print(min(4, 6))
# round lets you round a number
print(round(4.6))
print(round(3.2))
# you can import libraries, like from math import *
from math import *
# floor will round a number down, while ceil will round it up.
print(ceil(3.4))
print(floor(3.4))
# sqrt will let you get a square root of a number
print(sqrt(16))
