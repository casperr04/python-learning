# Lists allow to store a lot of data with []
friends = ["Kevin","David","Joe","Ken","Mary"]
# indexes     0        1      2
# You can put any information in a list, like booleans, lists or strings
print(friends)
print(friends[0])
print(friends[1])
# indexes read from the front to back with -1 etc
print(friends[-1])
# 1: lets you to get the index specified and all of the others after
print(friends[2:])
# you can modify strings in a list
friends[1] = "Mike"
print(friends[1])
